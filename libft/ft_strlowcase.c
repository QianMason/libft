#include "libft.h"

char	*ft_strlowcase(char *str)
{
	char *hold;

	hold = str;
	while (*str != '\0')
	{
		if (*str >= 65 && *str <= 90)
		{
			*str += 32;
		}
		str++;
	}
	str = hold;
	return (str);
}
